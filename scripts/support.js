function getWeekDay(day){
	var dt = new Date(day);

    var weekday = new Array(7);
    weekday[0] = "–í—Å";
    weekday[1] = "–†–Ω";
    weekday[2] = "–í—Ç";
    weekday[3] = "–°—Ä";
    weekday[4] = "–ß—Ç";
    weekday[5] = "–ü—Ç";
    weekday[6] = "–°–±";
    
    return weekday[dt.getDay()];
}

